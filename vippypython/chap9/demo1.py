# 字符串的驻留：内存中有相同的字符串不会重复创建，而是直接赋值已有字符串的内存地址
# a = sys.intern(b) 强制让a指向b的字符串值的内存地址
'''
  命令行交互式模式的驻留机制
    - 字符串长度为0或1
    - 符合标识符的字符串才行，举例：%不行
    - 只在编译时驻留，运行时不生效
    - [-5, 256] 之间的整数数字
'''

a = 'Python'
b = 'Python'
c = '''Python'''
# 内存地址是同一个
print(a, id(a)) # 4565138608
print(b, id(b)) # 4565138608
print(c, id(c)) # 4565138608